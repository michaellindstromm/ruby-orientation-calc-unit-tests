class Calculator
    """Performs the four basic mathematical operations

    Methods:
     add(number, number)
     subtract(number, number)
     multiply(number, number)
     divide(number,number)
    """

    def add(num1, num2)
        return num1 + num2
    end

    def sub(num1, num2)
        return num1 - num2
    end

    def mult(num1, num2)
        return num1 * num2
    end

    def div(num1, num2)
        return num1 / num2
    end

end